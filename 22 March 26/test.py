def add(a, b):
    """Return the sum of a and b."""
    return a + b

def subtract(a, b):
    """Return the difference of a and b (a - b)."""
    return a - b

# Example usage
if __name__ == "__main__":
    print("Addition:", add(5, 3))  # Output: 8
    print("Subtraction:", subtract(5, 3))  # Output: 2